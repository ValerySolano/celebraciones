    private void tablaCelebracionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCelebracionesMouseClicked
        // TODO add your handling code here:
        int fila = tablaCelebraciones.getSelectedRow();
        if (fila >= 0) {
            int idCelebracion = (int) tablaCelebraciones.getValueAt(fila, 0);
            Celebracion celebracionSeleccionada = null;
            for (Celebracion c : control.getLista()) {
                if (c.getIdCelebracion() == idCelebracion) {
                    celebracionSeleccionada = c;
                    break;
                }
            }
            if (celebracionSeleccionada != null) {
                UpsertCelebracion editarCelebracion = new UpsertCelebracion(this.control, celebracionSeleccionada);
                editarCelebracion.setVisible(true);
                editarCelebracion.addWindowListener(new java.awt.event.WindowAdapter() {
                    @Override
                    public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                        btnBuscarCelebracionActionPerformed(null);
                    }
                });
            }
        }
    }//GEN-LAST:event_tablaCelebracionesMouseClicked

