    private void aplicarBusqueda() {
        String paisSolicitad = txtBusqueda.getText().trim();
        if (paisSolicitad.isEmpty()) {
            jMensaje.setText("El campo de búsqueda no puede estar vacío");
        }
        ArrayList<Celebracion> celebracionesEncontradas = control.buscarCelebracionesPorPais(paisSolicitad);
        if (celebracionesEncontradas.isEmpty()) {
            jMensaje.setText("No se encontraron celebraciones para el país: " + paisSolicitad);
            DefaultTableModel model = (DefaultTableModel) tablaCelebraciones.getModel();
            model.setRowCount(0);
        } else {
            jMensaje.setText(celebracionesEncontradas.size() + " celebraciones encontradas para el país: " + paisSolicitad);
            DefaultTableModel model = (DefaultTableModel) tablaCelebraciones.getModel();
            model.setRowCount(0);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            for (Celebracion c : celebracionesEncontradas) {
                Object[] fila = new Object[]{
                    c.getIdCelebracion(),
                    sdf.format(c.getFecha()),
                    c.getDescripcion(),
                    c.getPais()
                };
                model.addRow(fila);
            }
        }
    }
